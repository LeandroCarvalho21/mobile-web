// formas de decremento 

let valor = 7;
console.log(valor);

valor = valor - 1;
console.log(`Segundo valor: ${valor}`);

valor--;
console.log(`Terceiro valor: ${valor}`);

valor -= 3;
console.log(`Quarto valor: ${valor}`);

//Multiplicação
valor *= 2 
console.log(`Valor mutiplicicado:  ${valor}`)

//Divisão
valor /= 4;
console.log(`Valor dividido por 4: ${valor}`);
