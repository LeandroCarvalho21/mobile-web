//formas de incrementar 
let valor = 7;

console.log(`Primeiro valor: ${valor}`); // valor igual a 7

valor = valor + 1 //incrementa mais 1
console.log(`Segundo valor: ${valor}`); // valor igual a 8

valor++ //incrementa mais 1
console.log(`Terceiro valor: ${valor}`); // valor igual a 9

valor++ //incrementa mais 1
console.log(`Quarto valor: ${valor}`); // valor igual a 10

valor+=3 //incrementa mais 3
console.log(`Quinto valor: ${valor}`); // valor igual a 10

