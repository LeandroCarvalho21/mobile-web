// formas de decremento 

let valor = 7;
console.log(`Valor: ${valor}`);

valor = valor - 1;
console.log(`Valor anterior menos 1: ${valor}`);

valor--;
console.log(`Valor anterior menos 1: ${valor}`);

valor -= 3;
console.log(`Valor anterior menos 3: ${valor}`);

//Multiplicação
valor *= 2
console.log(`Valor anterior mutiplicicado por 2:  ${valor}`)

//Divisão
valor /= 4;
console.log(`Valor anterior dividido por 4: ${valor}`);
