//Funções

//somar
function somar(num1, num2) {
    let resul = num1 + num2
    console.log("somar: ",resul);
    document.write("<br>")
    document.write("Resultado da soma: ", resul);
    document.write("<br>")
}

//chama a função somar
somar(0, 10);
somar(10, 10);
somar(10, 20);

