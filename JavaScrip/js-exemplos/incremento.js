//formas de incrementar 
let valor = 7;

console.log(`Valor da variavel: ${valor}`); // valor igual a 7

valor = valor + 1 //incrementa mais 1
console.log(`Valor anterior mais 1: ${valor}`); // valor igual a 8

valor++ //incrementa mais 1
console.log(`Valor anterior mais 1: ${valor}`); // valor igual a 9

valor++ //incrementa mais 1
console.log(`Valor anterior mais 1: ${valor}`); // valor igual a 10

valor+=3 //incrementa mais 3
console.log(`Valor anterior mais 3: ${valor}`); // valor igual a 13

